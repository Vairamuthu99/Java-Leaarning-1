package Abasicjava.Ln;

public class SimpleMaths {

	public static void main(String[] args) {

		int a = 1;
		int b = 5;
		System.out.println("The addition result is :" + (a + b));
		System.out.println("The subraction result is :" + (a - b));
		System.out.println("The multipication result is :" + (a * b));
		System.out.println("The division result is :" + (a / b));
	}
}
package MethodOverLoading.Learning;

public class AMethodOverLoadingCocept {
	
	/*
	 * Method Overloading:
	 * We can give same name for more  than one method
	 * Increase the readability
	 * 
	 * How are we going to distinguish between these two methods?
	 * 1.Number of parameters in both methods
	 * 2.Data types of the parameters in the both the methods
	 * 
	 * Note:
	 * Return type and access modifier does not play any specific role in method overloading
	 * 
	 * During compile time you decide which method to call
	 * Compile time polymorphism
	 * 
	 * You can also overload main method
	 * 
	 */
}
